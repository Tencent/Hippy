<template>
  <div id="demo-pull-header">
    <div class="toolbar">
      <button
        class="toolbar-btn"
        @click="scrollToNextPage"
      >
        <span>翻到下一页</span>
      </button>
      <button
        class="toolbar-btn"
        @click="scrollToBottom"
      >
        <span>翻动到底部</span>
      </button>
      <p class="toolbar-text">
        列表元素数量：{{ dataSource.length }}
      </p>
    </div>

    <!-- numberOfRows is necessary by iOS first screen rendering -->
    <ul
      id="list"
      ref="list"
      :numberOfRows="dataSource.length"
      @endReached="onEndReached"
      @scroll="onScroll"
    >
      /**
      * 下拉组件
      *
      * 事件：
      *   idle: 滑动距离在 pull-header 区域内触发一次，参数 contentOffset，滑动距离
      *   pulling: 滑动距离超出 pull-header 后触发一次，参数 contentOffset，滑动距离
      *   refresh: 滑动超出距离，松手后触发一次
      */
      <pull-header
        ref="pullHeader"
        class="ul-refresh"
        @idle="onIdle"
        @pulling="onPulling"
        @released="onRefresh"
      >
        <p class="ul-refresh-text">
          {{ refreshText }}
        </p>
      </pull-header>
      <li
        v-for="(ui, index) in dataSource"
        :key="index"
        class="item-style"
        :type="'row-' + ui.style"
      >
        <style-one
          v-if="ui.style === 1"
          :item-bean="ui.itemBean"
        />
        <style-two
          v-if="ui.style === 2"
          :item-bean="ui.itemBean"
        />
        <style-five
          v-if="ui.style === 5"
          :item-bean="ui.itemBean"
        />
      </li>
    </ul>
    <p
      v-show="loadingState"
      id="loading"
    >
      {{ loadingState }}
    </p>
  </div>
</template>

<script>
import Vue from 'vue';
import mockData from '../list-items/mock';
import '../list-items';

const STYLE_LOADING = 100;
const MAX_FETCH_TIMES = 50;
const REFRESH_TEXT = '下拉后放开，将会刷新';

export default {
  data() {
    return {
      loadingState: '',
      refreshText: REFRESH_TEXT,
      dataSource: [],
      scrollPos: {
        top: 0,
        left: 0,
      },
      Vue,
      STYLE_LOADING,
    };
  },
  mounted() {
    // *** isLoading 是加载锁，业务请照抄 ***
    // 因为 onEndReach 位于屏幕底部时会多次触发，
    // 所以需要加一个锁，当未加载完成时不进行二次加载
    this.isLoading = false;
    this.dataSource = [...mockData];
    if (Vue.Native) {
      this.$windowHeight = Vue.Native.Dimensions.window.height;
      console.log('Vue.Native.Dimensions.window', Vue.Native.Dimensions);
    } else {
      this.$windowHeight = window.innerHeight;
    }
  },
  methods: {
    mockFetchData() {
      return new Promise((resolve) => {
        setTimeout(() => {
          this.fetchTimes += 1;
          if (this.fetchTimes >= MAX_FETCH_TIMES) {
            return resolve(null);
          }
          return resolve(mockData);
        }, 300);
      });
    },
    onIdle() {
      this.refreshText = REFRESH_TEXT;
    },
    onPulling() {
      this.refreshText = '松手即可进行刷新';
    },
    onScroll(evt) {
      evt.stopPropagation();
      this.scrollPos = {
        top: evt.offsetY,
        left: evt.offsetX,
      };
    },
    async onRefresh() {
      // 重新获取数据
      this.refreshText = '刷新数据中请稍等，完成后将自动收起';
      const dataSource = await this.mockFetchData();
      await (new Promise(resolve => setTimeout(() => resolve(), 800)));
      this.dataSource = dataSource.reverse();
      // 要主动调用collapsePullHeader关闭pullHeader，否则可能会导致released事件不能再次触发
      this.$refs.pullHeader.collapsePullHeader({ time: 0 });
      this.refreshText = REFRESH_TEXT;
    },
    async onEndReached() {
      const { dataSource } = this;
      if (this.isLoading) {
        return;
      }
      this.isLoading = true;
      this.loadingState = '正在加载...';
      const newData = await this.mockFetchData();
      if (!newData) {
        this.loadingState = '没有更多数据';
        this.isLoading = false;
        return;
      }
      this.loadingState = '';
      this.dataSource = [...dataSource, ...newData];
      this.isLoading = false;
    },
    /**
     * 翻到下一页
     */
    scrollToNextPage() {
      // 因为布局问题，浏览器内 flex: 1 后也会超出窗口尺寸高度，所以这么滚是不行的。
      if (!Vue.Native) {
        alert('This method is only supported in Native environment.');
        return;
      }
      const { list } = this.$refs;
      const { scrollPos } = this;
      const top = scrollPos.top + this.$windowHeight - 200; // 偷懒假定内容区域为屏幕高度 - 200
      // CSSOM View standard - ScrollToOptions
      // https://www.w3.org/TR/cssom-view-1/#extensions-to-the-window-interface
      list.scrollTo({
        left: scrollPos.left,
        top,
      });
    },
    /**
     * 滚动到底部
     */
    scrollToBottom() {
      if (!Vue.Native) {
        alert('This method is only supported in Native environment.');
        return;
      }
      const { list } = this.$refs;
      list.scrollToIndex(0, list.childNodes.length - 1);
    },
  },
};
</script>

